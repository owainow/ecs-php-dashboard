	<h3>For frequently asked questions by competitors read below</h3>



	<h3> How do i enter into an event?</h3>
	<p> In order to enter into a competition you must visit the "View competitions" page this will allow a competitor to view all competitions. A competitior must select Book now in order to enter the event .</p>
					

	<h3> How do i fill in the booking form?</h3>
	<p> When a rider selects the "Book Now" button for an event on the "View competitions" page the user will be re-directed to a page with a booking form. All fields on the booking form must be filled in and completed, if these are not completed ECS is unable to process the booking and a user will be unable to proceed.</p>

	<h3> How do i enter more than one horse?</h3>
	<p> In order to process entries for multiple horses you must complete a booking form for each horse. This ensures a clear time is set for each round.</p>

	<h3> Where can i find the schedule for an event?</h3>
	<p> The schedule is processed and created once the entries for the event close. The schedule can then be downloaded from the "View Competitions" page. Riders can then check the time they are due to compete.</p>

	<h3> Is there a mobile platform?</h3>
	<p> At ECS we understand that competing can be very fast paced and leaves little time for logging in on a laptop to view a schedule. As a result we have made the website available on mobile devices. This will allow riders to enter into competitions and view competition schedules on the go.</p>
